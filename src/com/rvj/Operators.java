package com.rvj;

/***
 * Operators provides an organized way to declare specific
 * operation commands when the SRPN object iterates through a
 * string object. This is simply my personal preference.
 */
public class Operators {
    public static final char add = '+';
    public static final char subtract = '-';
    public static final char multiply = '*';
    public static final char divide = '/';
    public static final char power = '^';
    public static final char remainder = '%';
    public static final char random = 'r';
    public static final char displayStack = 'd';
    public static final char commenter = '#';
    public static final char spacer = ' ';
    public static final char equals = '=';

    public Operators() {
    }
}
